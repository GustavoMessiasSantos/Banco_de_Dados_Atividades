/*SELECT DO POKEMON COM SUAS FRAQUEZAS*/
SELECT 
    p.id AS id_pokemon,
    p.nome AS nome_pokemon,
    GROUP_CONCAT(f.nome SEPARATOR ', ') AS fraquezas_pokemon
FROM 
    tb_pokemon p
JOIN 
    tb_pokemon_fraqueza pf ON p.id = pf.id_pokemon
JOIN 
    tb_fraqueza f ON pf.id_fraqueza = f.id
GROUP BY 
    p.id;

 ####################################################################
 /*SELECT DOS POKEMONS COM SUAS HABILIDADES*/
SELECT 
    p.id AS numero_pokedex,
    p.nome AS nome,
    GROUP_CONCAT(h.nome SEPARATOR ' ou ') AS habilidades
FROM 
    tb_pokemon p
JOIN 
    tb_pokemon_habilidade ph ON p.id = ph.id_pokemon
JOIN 
    tb_habilidade h ON ph.id_habilidade = h.id
GROUP BY 
    p.id, p.nome;
