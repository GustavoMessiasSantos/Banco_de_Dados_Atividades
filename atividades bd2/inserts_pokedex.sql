/*SELECT DOS POKEMONS COM SEUS TIPOS*/
SELECT 
	p.id AS numero_dex,
    p.nome AS nome_pokemon,
    GROUP_CONCAT(t.nome SEPARATOR ', ') AS tipo_do_pokemon
FROM 
    tb_pokemon p
JOIN 
    tb_pokemon_tipo pt ON p.id = pt.id_pokemon
JOIN 
    tb_tipo t ON pt.id_tipo = t.id
GROUP BY 
    p.id;
#########################################################################
/*SELECT DOS POKEMONS COM SUAS EVOLUÇÕES*/
SELECT 
    p.id AS numero_pokedex,
    p.nome AS nome,
    e.nome AS evolução
FROM 
    tb_pokemon p
JOIN 
    tb_pokemon_evolucao pe ON p.id = pe.id_pokemon
JOIN 
    tb_evolucao e ON pe.id_evolucao = e.id;

##################################################################
/*SELECT DOS POKEMONS COM SEU TIPO, HABILIDADE, DESC HABILIDADE*/
SELECT 
    p.id AS numero_pokedex,
    p.nome AS nome_pokemon,
    GROUP_CONCAT(DISTINCT t.nome ORDER BY t.nome ASC SEPARATOR ' ou ') AS tipo,
    h.nome AS habilidade,
    h.descricao AS descricao_habilidade
FROM 
    tb_pokemon p
JOIN 
    tb_pokemon_tipo pt ON p.id = pt.id_pokemon
JOIN 
    tb_tipo t ON pt.id_tipo = t.id
JOIN 
    tb_pokemon_habilidade ph ON p.id = ph.id_pokemon
JOIN 
    tb_habilidade h ON ph.id_habilidade = h.id
GROUP BY 
    p.id, p.nome, h.nome, h.descricao
ORDER BY 
    p.id;
